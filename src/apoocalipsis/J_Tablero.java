    private String ponerTexto(Casilla c) {
        if (c.hayAlgunSupervivienteVivo() && c.hayAlgunZombi()) {
            return "/";
        } else if (c.hayAlgunZombi()) {
            return "Z";
        } else if (c.hayAlgunSupervivienteVivo()) {
            return "S";
        } else {
            return "";
        }
    }
    
